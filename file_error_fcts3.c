#include "file_parse.h"

int check_range(char *string)
{
    if (string[0] >= '0' && string[0] <= '2'
        && string[1] >= '0' && string[1] <= '5'
        && string[2] >= '0' && string[2] <= '5')
        return (1);
    return (0); 
}